# =============================================
# 第二节：第一个程序 - Hello World
# =============================================

# print() 是 Python 的一个"命令"，意思是"打印"
# 它会把括号里的内容显示在屏幕上
# 注意：引号和括号都要用英文的！

print("Hello, World!")
print("你好，Python！")

# "Hello, World!" 是编程界的传统
# 每个程序员学新语言时，第一个程序都是这个

# =============================================
# 第三节：什么是变量？
# =============================================

# 【什么是变量？】
# 变量就是一个"盒子"，用来存放东西
# 你可以给盒子起名字，然后把东西放进去
# 以后想用这个东西，只需要叫盒子的名字

# 【怎么创建变量？】
# 语法：名字 = 东西
# 等号 = 的意思是"把右边的东西放到左边的盒子里"

name = "小明"      # 创建一个变量 name，里面放 "小明"
age = 18            # 创建一个变量 age，里面放 18

# 【怎么使用变量？】
# 直接写变量的名字，就可以使用里面的值
print("我叫", name)
print("我今年", age, "岁")

# 【变量可以改变】
name = "小红"       # 把 name 里的东西换成 "小红"
print("现在叫", name)

# 【变量可以参与计算】
age = 18
age = age + 1       # 把 age 里的值加 1，再放回去
print("现在年龄是", age)   # 19

# =============================================
# 第四节：简单计算
# =============================================

# Python 可以当计算器用！

# 加法 +
print(3 + 5)        # 8

# 减法 -
print(10 - 3)       # 7

# 乘法 *
print(4 * 5)        # 20

# 除法 /（总是返回小数）
print(10 / 3)       # 3.3333...

# 用变量计算
a = 10
b = 3
print(a + b)        # 13
print(a * b)        # 30

# =============================================
# 第五节：用户输入 input()
# =============================================

# input() 会让程序停下来，等用户输入文字
# 用户按回车后，程序继续运行

# 基本用法（注意：下面这行被注释掉了，因为需要用户输入）
# name = input("请输入你的名字：")
# print("你好，" + name + "！")

# 【重要提醒】
# input() 返回的总是"字符串"（文字）！
# 即使用户输入的是数字，也会被当作文字
# 后面的课程会教你如何转换成数字

# =============================================
# 第六节：条件判断 if（简介）
# =============================================
# 条件判断让程序能根据情况做不同的事。
# 详细内容请看第 06 课《条件语句》。

age = 16
if age >= 18:
    print("你已经成年了")
else:
    print("你还未成年")

