# 第一节：变量的命名规则
# =============================================

# 上节课我们学了怎么创建变量，比如 name = "小明"
# 但是给变量起名字是有规则的，就像给人起名字一样

# 【规则1：只能包含字母、数字、下划线】
my_name = "小明"      # ✓ 正确：使用下划线
my_age = 18           # ✓ 正确：使用下划线
my_123 = "可以"       # ✓ 正确：名字里可以包含数字
_private = "私有"     # ✓ 正确：下划线开头也行

# 【规则2：不能以数字开头】
# 123name = "错误"    # ✗ 错误！不能以数字开头
# 2age = 18           # ✗ 错误！

# 【规则3：不能使用 Python 保留字】
# 保留字是 Python 自己在用的词，你不能拿来当变量名：
# if, else, for, while, def, class, import, print, True, False...
# print = "错误"      # ✗ 错误！print 是保留字
# if = "错误"         # ✗ 错误！if 是保留字

# 【规则4：区分大小写】
Name = "大写N"
name = "小写n"
# 这是两个不同的变量！Python 分得清清楚楚

# 【起名字的好习惯】
# 1. 名字要有意义，让人一看就知道是什么
student_name = "小明"   # ✓ 好名字：一看就知道是学生姓名
x = "小明"              # ✗ 不好：过两天你自己都忘了 x 是什么

# 2. 用下划线连接多个单词（Python 的官方推荐风格）
my_first_name = "明"    # ✓ 好：Python 风格（snake_case）
myFirstName = "明"      # 也能用，但这是 Java/JS 的风格，Python 不推荐

# 3. 常量用全大写（后面会详细讲）
PI = 3.14159            # 圆周率
MAX_SCORE = 100         # 最大分数

# =============================================
# 第二节：数据类型详解
# =============================================

# 【什么是数据类型？】
# 就像生活中有不同的东西：
# - 数字（年龄、成绩、身高）
# - 文字（名字、地址、爱好）
# - 对错（是学生吗？及格了吗？）
#
# Python 把它们分成不同的"类型"

# ---------- 整数 int ----------
# 就是没有小数点的数字，可以是正数、负数、零

age = 18              # 正整数
score = 100           # 正整数
count = 0             # 零
temperature = -5      # 负整数

print("=== 整数示例 ===")
print("年龄：", age)
print("分数：", score)
print("数量：", count)
print("温度：", temperature)

# ---------- 浮点数 float ----------
# 就是有小数点的数字，也叫"小数"或"实数"

height = 1.75         # 身高
weight = 65.5         # 体重
pi = 3.14159          # 圆周率
price = 9.99          # 价格

print("\n=== 浮点数示例 ===")
print("身高：", height, "米")
print("体重：", weight, "公斤")
print("圆周率：", pi)
print("价格：", price, "元")

# 【小知识】
# 在 Python 中，整数可以无限大！
# 比如 1000000000000000000000000000000000000 也是合法的整数
# 但浮点数有精度限制，比如 0.1 + 0.2 不等于 0.3（后面会学到原因）

# ---------- 字符串 str ----------
# 就是文字，必须用引号包起来
# 可以用双引号 " " 或单引号 ' '，效果一样

name = "小明"         # 用双引号
greeting = '你好'     # 用单引号也可以
empty = ""            # 空字符串（里面什么都没有，但也是字符串）
with_space = "  "     # 只有空格的字符串

print("\n=== 字符串示例 ===")
print("名字：", name)
print("问候：", greeting)
print("空字符串长度：", len(empty))  # 0

# 【多行字符串】
# 用三个引号 """ 或 ''' 可以写多行文字
poem = """
静夜思
床前明月光，
疑是地上霜。
举头望明月，
低头思故乡。
"""
print(poem)

# ---------- 布尔 bool ----------
# 就是"真"或"假"，只有两个值：True 和 False
# 注意：T 和 F 要大写！

is_student = True     # 是学生
is_rich = False       # 不富有
is_happy = True       # 开心

print("\n=== 布尔示例 ===")
print("是学生吗？", is_student)
print("富有吗？", is_rich)
print("开心吗？", is_happy)

# 【布尔从哪来？】
# 布尔值通常来自比较运算
print("\n18 >= 18 的结果是：", 18 >= 18)   # True
print("10 > 20 的结果是：", 10 > 20)       # False
print("5 == 5 的结果是：", 5 == 5)         # True
print("3 != 3 的结果是：", 3 != 3)         # False

# =============================================
# 第三节：查看数据类型
# =============================================

# 【怎么知道一个变量是什么类型？】
# 使用 type() 函数

name = "小明"
age = 18
height = 1.75
is_student = True

print("\n=== 查看数据类型 ===")
print("name 的类型：", type(name))         # <class 'str'>
print("age 的类型：", type(age))           # <class 'int'>
print("height 的类型：", type(height))     # <class 'float'>
print("is_student 的类型：", type(is_student))  # <class 'bool'>

# 【类型的名字】
# str = string（字符串）
# int = integer（整数）
# float = floating point（浮点数，浮点数）
# bool = boolean（布尔）

# =============================================
# 第四节：类型转换
# =============================================

# 【什么是类型转换？】
# 就是把一种类型变成另一种类型
# 比如把数字变成文字，或者把文字变成数字
# 这在实际编程中非常常用！

# ---------- 字符串转整数：int() ----------
age_str = "18"           # 这是字符串（文字）
age_int = int(age_str)   # 转换成整数（数字）
print("\n=== 类型转换示例 ===")
print("字符串：", age_str, type(age_str))
print("整数：", age_int, type(age_int))
# 现在可以做数学运算了！
print("明年：", age_int + 1)

# ---------- 字符串转浮点数：float() ----------
height_str = "1.75"
height_float = float(height_str)
print("字符串：", height_str, type(height_str))
print("浮点数：", height_float, type(height_float))

# ---------- 整数转字符串：str() ----------
age = 18
age_str = str(age)
print("整数：", age, type(age))
print("字符串：", age_str, type(age_str))
# 转成字符串后就可以拼接了
print("我今年" + age_str + "岁")  # 字符串才能用 + 拼接

# ---------- 整数转浮点数 ----------
x = 10
y = float(x)
print("整数：", x, type(x))
print("浮点数：", y, type(y))

# ---------- 浮点数转整数（会丢失小数部分！） ----------
x = 3.7
y = int(x)
print("浮点数：", x)
print("整数：", y)  # 注意：小数部分被截掉了，不是四舍五入！
# 3.7 变成 3，不是 4！

# ---------- 布尔转整数 ----------
# True 变成 1，False 变成 0
print("True 转整数：", int(True))    # 1
print("False 转整数：", int(False))  # 0

# ---------- 不能转换的情况 ----------
# int("hello")  # ✗ 报错！"hello" 不是数字，没法转
# int("3.14")   # ✗ 报错！不能直接把小数字符串转整数
# 正确做法：int(float("3.14"))  → 先转浮点数，再转整数 → 3

# =============================================
# 第五节：变量遮蔽（Shadowing）
# =============================================

# 【什么是变量遮蔽？】
# 就是用同一个变量名，给它赋一个新值
# 旧的值就被"覆盖"了，就像盒子里换了新东西

print("\n=== 变量遮蔽 ===")

# 【示例1：基本遮蔽】
x = 10
print(f"第一个 x = {x}")   # 10

x = 20                     # 用新值覆盖旧值
print(f"第二个 x = {x}")   # 20
# 注意：旧的 10 已经不存在了！

# 【示例2：可以改变类型！】
# Python 的变量不像其他语言那么"死板"
# 你可以把一个整数变量变成字符串
x = 10                     # 整数
print(f"x = {x}, 类型 = {type(x)}")

x = "hello"                # 变成字符串！
print(f"x = {x}, 类型 = {type(x)}")

# 【示例3：实际应用场景】
# input() 返回字符串，我们用同名变量存转换后的结果
input_str = "42"           # 假设这是用户输入的内容（字符串）
print(f"字符串：{input_str}")

input_num = int(input_str)  # 转换成整数，用新变量存
print(f"整数：{input_num}")
print(f"可以做运算了：{input_num + 8}")  # 50

# 【变量遮蔽的注意事项】
# 虽然 Python 允许你随时改变变量类型，但这不是好习惯！
# 建议：一个变量只存一种类型的数据
# 比如 age 就应该一直是整数，不要突然变成字符串

# =============================================
# 第六节：f-string 高级用法
# =============================================

# 上节课我们学了最简单的 f-string：f"我叫{name}"
# 这节课学一些高级技巧

print("\n=== f-string 高级用法 ===")

# ---------- 控制小数位数 ----------
score = 95.678
print(f"原始：{score}")          # 95.678
print(f"保留1位：{score:.1f}")   # 95.7（四舍五入）
print(f"保留2位：{score:.2f}")   # 95.68
print(f"保留0位：{score:.0f}")   # 96（四舍五入到整数）

# ---------- 在 {} 里放表达式 ----------
age = 18
print(f"明年{age + 1}岁")       # 可以做计算
print(f"两年后{age + 2}岁")     # 也可以做更复杂的计算

# ---------- 在 {} 里放条件表达式 ----------
age = 16
print(f"成年了吗？{'成年了' if age >= 18 else '未成年'}")
# 这叫"三元表达式"：值1 if 条件 else 值2

# ---------- 数字格式化 ----------
big_number = 1234567890
print(f"原始：{big_number}")             # 1234567890
print(f"加千位分隔符：{big_number:,}")   # 1,234,567,890

# ---------- 百分比 ----------
rate = 0.856
print(f"正确率：{rate:.1%}")   # 85.6%

# ---------- 对齐 ----------
for name, score in [("小明", 95), ("小红", 100), ("小刚", 78)]:
    # <10 表示左对齐，占10个字符宽度
    print(f"{name:<10} {score:>5}分")

# ---------- 字符串的对齐 ----------
text = "Python"
print(f"[{text:<20}]")   # 左对齐，宽度20
print(f"[{text:>20}]")   # 右对齐，宽度20
print(f"[{text:^20}]")   # 居中，宽度20

