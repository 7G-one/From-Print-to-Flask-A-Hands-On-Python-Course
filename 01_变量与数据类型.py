# 第七节：常量与命名约定
# =============================================

# 【什么是常量？】
# 常量就是"不应该被改变"的值
# 比如圆周率 PI = 3.14159，它永远不会变

# 【Python 没有真正的常量！】
# 其他语言（如 C、Java）有 const 关键字
# 但 Python 没有！Python 信任程序员，觉得你会自觉

# 【Python 的命名约定：全大写 = 常量】
# 这是一个"约定"，不是强制的
# 看到全大写的变量名，程序员就知道"这个不该改"

PI = 3.14159              # 圆周率，不该改
MAX_SCORE = 100           # 最大分数，不该改
DEFAULT_NAME = "匿名"     # 默认名字，不该改
DATABASE_URL = "localhost:5432"  # 数据库地址，不该改

# 虽然 Python 允许你改（PI = 0），但这是"违规操作"
# 就像在图书馆大声说话——没人抓你，但大家都知道你不对

# 【命名风格总结】
# 变量名（普通变量）：小写 + 下划线
#   student_name, total_score, is_valid
#
# 常量名（不该改的值）：全大写 + 下划线
#   PI, MAX_SCORE, DEFAULT_NAME
#
# 类名（后面会学）：每个单词首字母大写
#   StudentInfo, GameScore

